REGION_CODES_BY_NAME = {name.casefold(): code for code, name in US_REGIONS.items()}


def canonical_us_region(value: str) -> str | None:
    cleaned = " ".join(value.strip().split())
    code = cleaned.upper()
    if code in US_REGIONS:
        return code
    return REGION_CODES_BY_NAME.get(cleaned.casefold())


def region_search_terms(value: str) -> tuple[str, ...]:
    cleaned = " ".join(value.strip().split())
    canonical = canonical_us_region(cleaned)
    if canonical is None:
        return (cleaned,)
    return (canonical, US_REGIONS[canonical])
